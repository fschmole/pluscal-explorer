def load_traces(constant_names: list[str] = None) -> dict:
    """Load all trace .puml files and expand aliases.

    Each entry in the returned dict has:
      - puml_text: str  (PlantUML source)
      - parameters: dict (reconstructed from filename tag)
    """
    if constant_names is None:
        constant_names = []
    traces = {}
    for f in sorted(TRACES_DIR.glob("*.puml")):
        if f.stem.startswith("_"):
            continue
        key = f.stem
        puml_text = f.read_text(encoding="utf-8")
        parts = key.split(".")
        parameters = dict(zip(constant_names, parts)) if constant_names else {}
        traces[key] = {"puml_text": puml_text, "parameters": parameters}

    # Expand aliases: { aliased_key: canonical_key }
    alias_file = TRACES_DIR / "_aliases.json"
    if alias_file.exists():
        aliases = json.loads(alias_file.read_text(encoding="utf-8"))
        for alias_key, canonical_key in aliases.items():
            if alias_key not in traces and canonical_key in traces:
                parts = alias_key.split(".")
                parameters = dict(zip(constant_names, parts)) if constant_names else {}
                traces[alias_key] = {
                    "puml_text": traces[canonical_key]["puml_text"],
                    "parameters": parameters,
                }

    return traces


def build_flow_tree(traces: dict, constant_names: list[str]) -> dict:
    """Build nested dict from trace parameters, generic for any set of constants.

    For N constants, builds an N-level nested dict where each level corresponds
    to one constant (in order).  The leaf level is a list of values for the
    last constant.

    Example for MESI (Operation, InitState, RemoteState):
        {
            "Read": {"I": ["M", "E", "S", "I"], "M": ["I"], ...},
            "Write": {"I": ["M", "E", "S", "I"], ...},
        }
    """
    if not constant_names:
        return {}
    tree: dict = {}
    for key, data in traces.items():
        p = data["parameters"]
        vals = [p.get(c, "") for c in constant_names]
        if not all(vals):
            continue
        # Walk/create nested dicts for all but last constant
        node = tree
        for v in vals[:-1]:
            node = node.setdefault(v, {})
        # Last level is a list
        if not isinstance(node.get(vals[-1]), list):
            if vals[-1] not in node:
                node[vals[-1]] = True  # leaf marker
        # We also want to record the trace exists for this combo
        # Actually, for N=1 the tree is {val: True}, for N>1 last level is a list
    # Simpler approach: just record all combos as tag -> True, and let JS handle it
    # Actually let me do this cleanly:
    tree = {}
    for key, data in traces.items():
        p = data["parameters"]
        vals = [p.get(c, "") for c in constant_names]
        if not all(vals):
            continue
        if len(vals) == 1:
            tree.setdefault(vals[0], True)
        else:
            node = tree
            for v in vals[:-2]:
                node = node.setdefault(v, {})
            # Second-to-last level maps to list of last values
            node.setdefault(vals[-2], [])
            if vals[-1] not in node[vals[-2]]:
                node[vals[-2]].append(vals[-1])
    return tree


def build_full_flow_tree(config) -> dict:
    """Build nested dict from ALL constant combos (cartesian product).

    Unlike build_flow_tree() which only includes combos with traces,
    this includes every combination so dropdowns are fully populated.
    """
    constant_names = config.constant_names
    if not constant_names:
        return {}
    tree: dict = {}
    for combo in config.all_combos():
        vals = [str(v) for v in combo]
        if len(vals) == 1:
            tree.setdefault(vals[0], True)
        else:
            node = tree
            for v in vals[:-2]:
                node = node.setdefault(v, {})
            node.setdefault(vals[-2], [])
            if vals[-1] not in node[vals[-2]]:
                node[vals[-2]].append(vals[-1])
    return tree


