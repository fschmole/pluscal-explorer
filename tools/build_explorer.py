def _render_branding_footer(branding: dict) -> str:
    """Render the branding block (classification, contact, AI disclaimer).
    Returns empty string if branding is empty/None — clean for open-source models."""
    if not branding:
        return ""
    parts = []
    parts.append('<div style="margin-top:10px; padding-top:8px; border-top:1px solid #334155;">')
    classification = branding.get("classification")
    if classification:
        color = branding.get("classificationColor", "#f85149")
        parts.append(f'<div style="font-weight:700; color:{color}; letter-spacing:1px; font-size:11px; text-transform:uppercase;">{classification}</div>')
    contact = branding.get("contact")
    if contact:
        label = contact.get("label", "Contact")
        email = contact.get("email", "")
        if email:
            parts.append(f'<div style="margin-top:2px;">{label} <a href="mailto:{email}" style="color:#7dd3fc; text-decoration:none;">{email}</a></div>')
    ai_disclaimer = branding.get("aiDisclaimer")
    if ai_disclaimer:
        parts.append(f'<div style="margin-top:4px; font-style:italic; font-size:10px; line-height:1.4;">{ai_disclaimer}</div>')
    parts.append('</div>')
    return "\n".join(parts)


def _render_footer_links(branding: dict) -> str:
    """Render footer links from branding config (custom links)."""
    if not branding:
        return ""
    parts = []
    footer_links = branding.get("footerLinks", [])
    for link in footer_links:
        label = link.get("label", "")
        url = link.get("url", "")
        if label and url:
            parts.append(f'<a href="{url}" style="color:#7dd3fc; text-decoration:none;" target="_blank">&#128214; {label}</a><br>')
    return "\n".join(parts)


