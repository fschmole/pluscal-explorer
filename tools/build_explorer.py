def main_build(config) -> None:
    """Build HTML explorer using a PcalConfig object.

    Called by build.py's `cmd_build()`. The caller sets SCRIPT_DIR,
    DISTRIB_DIR, TRACES_DIR, and OUTPUT_HTML before calling this.
    """
    constant_names = config.constant_names

    print(f"\n1. Loading traces from {TRACES_DIR}")
    all_traces = load_traces(constant_names)
    print(f"   {len(all_traces)} PlantUML traces loaded")

    # Build full flow tree from ALL constant combos (not just traces)
    flow_tree = build_full_flow_tree(config)
    top_key = constant_names[0] if constant_names else "flows"
    print(f"   Flow tree: {len(flow_tree)} {top_key} values (all combos)")

    # Compute invalid and skipped combo tag sets for JS
    # Invalid takes priority: skipped set excludes combos already in invalid
    invalid_set = config.expanded_invalid_set()
    invalid_combos = sorted(config.combo_tag(c) for c in invalid_set)
    skipped_combos = sorted(config.combo_tag(c) for c in config.expanded_skip_set() if c not in invalid_set)
    print(f"   {len(invalid_combos)} invalid, {len(skipped_combos)} skipped combos")

    # Compute channel_styles from config
    channels = config.resolve_channels(SCRIPT_DIR)
    config_colors = config.channel_colors or {}
    _PALETTE = [
        ("#2563eb", "→"), ("#16a34a", "→"), ("#dc2626", "→"),
        ("#9333ea", "→"), ("#ea580c", "→"), ("#0891b2", "→"),
        ("#d946ef", "→"), ("#65a30d", "→"),
    ]
    channel_styles = {}
    for i, key in enumerate(channels):
        if key in config_colors:
            cc = config_colors[key]
            stroke = cc.get("stroke", _PALETTE[i % len(_PALETTE)][0])
            label = cc.get("label", _PALETTE[i % len(_PALETTE)][1])
        else:
            stroke, label = _PALETTE[i % len(_PALETTE)]
        channel_styles[key] = {
            "stroke": stroke,
            "label": label,
            "name": key.replace("_", " ").title(),
        }

    pcal_path = SCRIPT_DIR / config.pcal
    pcal_source = pcal_path.read_text(encoding="utf-8") if pcal_path.exists() else ""

    print(f"\n2. Building index.html")
    html = build_html(
        all_traces, flow_tree,
        pcal_source,
        title=config.title,
        branding=config.branding,
        pcal_filename=config.pcal,
        constant_names=constant_names,
        plantuml_server=config.plantuml_server or "",
        tlc_server=config.tlc_server or "http://127.0.0.1:18080",
        channel_styles=channel_styles,
        participants=config.participants,
        invalid_combos=invalid_combos,
        skipped_combos=skipped_combos,
    )

    OUTPUT_HTML.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_HTML.write_text(html, encoding="utf-8")
    size_kb = OUTPUT_HTML.stat().st_size / 1024
    print(f"   Output: {OUTPUT_HTML} ({size_kb:,.0f} KB)")

    # Copy server collateral and create server.zip
    tools_dir = Path(__file__).resolve().parent
    server_files = {
        "tlc_server.py":  tools_dir / "tlc_server.py",
        "tlc_sweep.py":   tools_dir / "tlc_sweep.py",
        "pcal_config.py": tools_dir / "pcal_config.py",
        "tla2tools.jar":  tools_dir / "tla2tools.jar",
    }
    # Model-specific files
    config_file = SCRIPT_DIR / f"{config.module}.explorer.json"
    if config_file.exists():
        server_files[config_file.name] = config_file
    if pcal_path.exists():
        server_files[pcal_path.name] = pcal_path

    # Build server.zip containing everything needed to run tlc_server.py
    zip_path = DISTRIB_DIR / "server.zip"
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for arcname, src in server_files.items():
            if src.exists():
                zf.write(src, arcname)
    zip_kb = zip_path.stat().st_size / 1024
    print(f"   Server bundle: {zip_path} ({zip_kb:,.0f} KB)")

    # Build DOCUMENTATION.html from Markdown (search repo root, then model dir)
    doc_md = None
    for candidate in [tools_dir.parent / "DOCUMENTATION.md",
                      SCRIPT_DIR / "DOCUMENTATION.md"]:
        if candidate.exists():
            doc_md = candidate
            break
    if doc_md:
        print(f"\n3. Building DOCUMENTATION.html from {doc_md.name}")
        doc_html = build_documentation_html(
            doc_md,
            title=f"{config.title} — Documentation",
            branding=config.branding,
        )
        doc_out = DISTRIB_DIR / "DOCUMENTATION.html"
        doc_out.write_text(doc_html, encoding="utf-8")
        doc_kb = doc_out.stat().st_size / 1024
        print(f"   Output: {doc_out} ({doc_kb:,.0f} KB)")
    else:
        print("\n3. No DOCUMENTATION.md found — skipping documentation build")


